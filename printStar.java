
public class printStar {

	public static void main(String[] args) {
		 for(int i=1;i<=10;i++){
	        	for(int k=50; k>=i; k--){
	        	System.out.print(" ");
	        	}
	            for(int j=0; j<i; j++){
	                    System.out.print("*");
	                    System.out.print(" ");
	            }
	           
	            System.out.println(" ");
	    }


	}

}
